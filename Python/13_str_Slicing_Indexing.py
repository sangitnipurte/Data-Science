name = "Sangit"
print(name[0:2]) #goes form 0 to 2-1 is 0 to 1

print(name[2:-1]) # Same as name[2:4]

#slicing with steps
print(name[0:6:2]) # means skip character in this print statement we skip 2nd character then 4th then 6th

print(name[:4])# replace with first empty number with 0 ie 0 to 4

print(name[1:])# replace with last empty number with length -1 